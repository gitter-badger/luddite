GBuff *luddite::gbuff_cube()
{
    GBuff *gbuff = new GBuff();
    
    DrawVert *cubeVert = gbuff->addVerts( NUM_CUBE_VERTS );
    
    for (int i=0; i < NUM_CUBE_VERTS; ++i)
    {
        cubeVert->m_pos = vec3f( _cubeVertData[i*6 + 0],
                                 _cubeVertData[i*6 + 1],
                                 _cubeVertData[i*6 + 2] );
 
        cubeVert->m_nrm = vec3f( _cubeVertData[i*6 + 3],
                                 _cubeVertData[i*6 + 4],
                                 _cubeVertData[i*6 + 5] );
        
        cubeVert++;
    }

    
    return gbuff;
}
